							<h2>Get a ride in Volos</h2>
							<p class="lead">Volos is one of the largest and most attractive cities in Greece as well as one of the country’s most prominent ports. The modern-day city, built near the site of ancient Iolcos, dominates the region of Magnesia from its position at the foot of Mount Pelion overlooking the Pagasetic Gulf. This is one of the most beautiful areas in Thessaly, in the centre of Greece (Volos is situated 320km N of Athens and 219km S of Thessaloniki) combining the allure of the sea with the mystical charm of the Centaurs’ mountain.</p>
							<p>The area’s mythical background captivates the visitor’s imagination. Magnesia features in some of the most remarkable tales in Greek mythology. The progenitor of its peoples was Magnes, son of Zeus and Aeolus, yet Magnesia is believed to be the birthplace of a strange race, the Centaurs, beings which were part equine, part human and originated from the union of Nephele and Ixion. Asclepius and Achilles were, among others, pupils of the Centaur Chiron. The famous Argonaut expedition, one of the greatest epics in the Greek mythological cycle, set sail from the Pagasetic Gulf.</p>
							<p>Volos is one of the most beautiful cities in Greece. The wealth of the region and its long history have left their mark on the city’s architecture, which exudes confidence and grandeur. The numerous mansions, municipal halls, museumσ, early 20th-century industrial facilities, and splendid churches make touring Volos truly enjoyable.The sea creates a wonderful seafront with picturesque little restaurants, the famous tsipouradika, named after tsipouro, the traditional spirit of the area, which is perfect when combined with seafood teasers. Follow the paved Argonauts Avenue on the seafront and go all the way to the port on its western side, just as the locals love doing when in the mood for a romantic stroll with a stunning view to the open sea. Beyond doubt, this is the most impressive corner of the city, embellished not only by fishing boats and yachts, but also by an exact imitation of ancient ship Argo, constructed by faithfully following reports and ancient texts. The modern Argo, a brilliant feat of shipbuilding, will fill history lovers with excitement.</p>
