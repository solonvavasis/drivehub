							<h2>Get a ride in Athens</h2>
							<p class="lead">Athens is the historical capital of Europe, with a long history, dating from the first settlement in the Neolithic age. In the 5th Century BC (the “Golden Age of Pericles”) – the culmination of Athens’ long, fascinating history – the city’s values and civilization acquired a universal significance. Over the years, a multitude of conquerors occupied Athens, and erected unique, splendid monuments - a rare historical palimpsest. In 1834, it became the capital of the modern Greek state and in two centuries since it has become an attractive modern metropolis with unrivalled charm.
A large part of the town’s historic centre has been converted into a 3-kilometre pedestrian zone (the largest in Europe), leading to the major archaeological sites (“archaeological park”), reconstructing – to a large degree – the ancient landscape.</p>
							<p>The tour starts at the temple of Olympian Zeus (6th c. B.C.), one of the largest in antiquity and close by Hadrian’s Arch (131 A.D.), which forms the symbolic entrance to the city.  From there, walking along Dionysou Areopaghitou Street (on the south side of the Acropolis) you pass the ancient Theatre of Dionysos (5th c. B.C.) where most of the works by Sophocles, Euripides, Aeschylos and Aristophanes were performed.  Continuing, you will reach the ruins of the Asklepieion (5th c. B.C.) and the Stoa of Eumenes (2th c. B.C.) and from there the Odeion of Herodes Atticus, which was built in 161 A.D. and is nowadays the venue of the performances of the Athens Festival.
From there you climb up to the sacred rock of the Acropolis, the site of some of the most important masterpieces of worldwide architecture and art, the most renowned of which is the Parthenon temple.  Apart from this, also impressive are the Propylaea, the temple of the Athene Nike and the Erechtheion, while you must not skip a visit to the Museum, located close to the Parthenon.  Moreover, from the rock you have an impressive view of the city.</p>
							<p>Only 300m away from the sacred rock of Acropolis stands the impressive Acropolis Museum, one of the most important contemporary works of architecture in Athens. It is made of steel, glass and concrete and it houses 4,000 priceless finds from the Acropolis monuments that represent its history and function as the most important religious centre of ancient Athens.</p>
