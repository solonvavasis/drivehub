							<h2>Get a ride in Thessaloniki</h2>
							<p class="lead">Thessaloniki (520 km. north of Athens) is the second largest city of Greece and the most important centre of the area. Built near the sea (at the back of the Thermaïkos Gulf), it is a modern metropolis bearing the marks of its stormy history and its cosmopolitan character, which give it a special beauty and charm.
Take a tour in the centre of Thessaloniki and plan to visit its nearby destinations. Also, while being in Thessaloniki it is worth going up to Halkidiki.</p>
							<p>The Old City (Ano Polis), in which many notable examples of Ottoman and traditional Macedonian architecture still stand, alongside humble dwellings put up by the refugees who reached Thessaloniki in droves, after the Greek defeat in Asia Minor, in 1922.
The historical quarter of the Ladadika. In recent years, a series of interventions to rehabilitate the urban fabric have helped to enhance the Ladadika as a quarter for leisure pursuits.
The traditional markets: the Modiano, which is housed in a rectangular building of 1922, with pedimented facade and glass roof; the Kapani or Vlalis market; Athonos Square and the ‘Louloudadika’ (literally flower market).
Vasilissis Olgas Avenue, lined with many representative Neoclassical buildings and examples of late 19th century eclectic architecture.
The central Aristotelous Square, surrounded by monumental buildings and open to the waterfront for a width of 100 metres.</p>
							<p>The White Tower (15th century), the hallmark of the city.
The Mosques of the Hamza Bey Cami (15th century), the Aladja Imaret Cami (1484) and the Yeni Cami  (1902).
Hamams (turkish bathhouses): The Pazar Hamam (15th century), the Pasha Hamam (15th century), Bey Hamam (16th century), Yeni Hamam and the Yahudi Hamam.
Bezesteni, a rectangular building with lead-covered domes and four entraces was built in the late fifteenth century and operated as a cloth market. 
</p>
