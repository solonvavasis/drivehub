							<p class="lead">The city of Chania is built on the area of Minoan Kidonia, at the end of the homonym gulf between Akrotiri and Onicha peninsulas. It was the former capital city of Crete (from 1847 until 1972). Nowadays, it is the second largest city of Crete after Heraklion and capital of the homonym prefecture. 
Chania includes the old and new city. It is one of the most beautiful and picturesque cities in Greece and for food lovers, it's a paradise! 
Get familiar with the city of Chania by wandering around in its streets, visiting its museums and admiring the different architectural styles presenting the historical route of the city.</p>
							<p>After Arabs and Byzantines it was conquered by Venetians in 1252 and was given to Turks in 1669, later it was annexed to the rest of the Greek State on December 1913 under the administration of Eleftherios Venizelos and King Konstantinos the 1st. The old town is an integral settlement with visible boundaries set by the Venetian walls surrounding it.
Chania has daily boat connection with Piraeus port from Souda port (7 km). Chania is also connected with Athens by airplane which you can take from Akrotiri airport 15 km E of the city.</p>
							<p>The old town is built around the Venetian port and is also a relatively integral area where Venetian buildings and later Turkish elements compose a unique architectural style. Due to the historic center of Chania with its Venetian walls defining the borders between the old and new city and its ramparts, the city has been pronounced as preserved. It consists of five connected districts surrounding the Venetian port.</p>
